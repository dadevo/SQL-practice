CREATE DATABASE CodeSchool
GO

USE CodeSchool
GO

CREATE TABLE teachers (
    id INT IDENTITY(1,1) PRIMARY KEY,
    name VARCHAR(64),
)
GO

CREATE TABLE tracks (
    id CHAR(2) PRIMARY KEY,
    name VARCHAR(64),
    teacher_id int,
    FOREIGN KEY (teacher_id) REFERENCES teachers(id)
)
GO

CREATE TABLE classes (
    track_id CHAR(2),
    number int,
    start_date DATE,
    end_date DATE,
    FOREIGN KEY (track_id) REFERENCES tracks(id),
    PRIMARY KEY(track_id, number)
)
GO

CREATE TABLE students (
    id INT IDENTITY(1,1) PRIMARY KEY,
    name VARCHAR(64),
    birthday DATE,
    track_id CHAR(2),
    track_number int, 
    FOREIGN KEY (track_id, track_number) REFERENCES classes(track_id, number),
)
GO

CREATE TABLE scores (
    student_id INT,
    "linux" TINYINT,
    "python" TINYINT,
    "databases" TINYINT,
    "networking" TINYINT,
    "security" TINYINT,
    "java" TINYINT,
    "version control" TINYINT,
    FOREIGN KEY (student_id) REFERENCES students(id),
)
GO

